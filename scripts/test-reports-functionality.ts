async function testReportTypes() {
  console.log('\n6. 📊 Testing Different Report Types...')
  
  try {
    const superAdmin = await prisma.user.findFirst({
      where: { role: 'SUPER_ADMIN' }
    })

    if (!superAdmin) {
      console.log('❌ No super admin found')
      return
    }

    const reportTypes = ['ANALYTICS', 'FINANCIAL', 'ACADEMIC', 'USER_ACTIVITY', 'SYSTEM_HEALTH', 'CUSTOM']
    const createdReports = []

    for (const type of reportTypes) {
      try {
        const report = await prisma.report.create({
          data: {
            title: `Test ${type} Report`,
            description: `Test report for ${type} type`,
            type: type as any,
            status: 'COMPLETED',
            content: JSON.stringify({
              [type.toLowerCase()]: {
                testData: `This is test data for ${type} report`,
                timestamp: new Date().toISOString()
              }
            }),
            generatedBy: superAdmin.id
          }
        })
        
        createdReports.push(report)
        console.log(`✅ Created ${type} report: ${report.id}`)
      } catch (error) {
        console.log(`❌ Failed to create ${type} report:`, error)
      }
    }

    console.log(`✅ Created ${createdReports.length} test reports of different types`)
    
    // Clean up test reports
    for (const report of createdReports) {
      try {
        await prisma.report.delete({ where: { id: report.id } })
      } catch (error) {
        console.log(`⚠️ Could not clean up report ${report.id}`)
      }
    }
    
    console.log('✅ Cleaned up test reports')
  } catch (error) {
    console.error('❌ Error testing report types:', error)
  }
}

async function testReportStatistics() {
  console.log('\n7. 📈 Testing Report Statistics...')
  
  try {
    const totalReports = await prisma.report.count()
    console.log(`📊 Total reports in database: ${totalReports}`)
    
    const reportsByType = await prisma.report.groupBy({
      by: ['type'],
      _count: { type: true }
    })
    
    console.log('📊 Reports by type:')
    reportsByType.forEach(group => {
      console.log(`   ${group.type}: ${group._count.type}`)
    })
    
    const reportsByStatus = await prisma.report.groupBy({
      by: ['status'],
      _count: { status: true }
    })
    
    console.log('📊 Reports by status:')
    reportsByStatus.forEach(group => {
      console.log(`   ${group.status}: ${group._count.status}`)
    })
    
    const publicReports = await prisma.report.count({ where: { isPublic: true } })
    const privateReports = await prisma.report.count({ where: { isPublic: false } })
    
    console.log(`📊 Public reports: ${publicReports}`)
    console.log(`📊 Private reports: ${privateReports}`)
    
  } catch (error) {
    console.error('❌ Error getting report statistics:', error)
  }
}

