async function testReportDeleting(reportId: string) {
  console.log('\n4. 🗑️ Testing Report Deletion...')
  
  try {
    // First verify the report exists
    const reportExists = await prisma.report.findUnique({
      where: { id: reportId }
    })

    if (!reportExists) {
      console.log('❌ Report not found for deletion')
      return false
    }

    console.log(`✅ Report found: ${reportExists.title}`)
    
    // Delete the report
    await prisma.report.delete({
      where: { id: reportId }
    })

    console.log('✅ Report deleted successfully')
    
    // Verify deletion
    const deletedReport = await prisma.report.findUnique({
      where: { id: reportId }
    })

    if (deletedReport) {
      console.log('❌ Report still exists after deletion')
      return false
    } else {
      console.log('✅ Report deletion verified - report no longer exists')
      return true
    }
  } catch (error) {
    console.error('❌ Error deleting report:', error)
    return false
  }
}

