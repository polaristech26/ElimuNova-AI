#!/usr/bin/env tsx

/**
 * Test script to verify all reports functionality including CRUD operations
 */

import { PrismaClient } from '@prisma/client'

const prisma = new PrismaClient()

console.log('🔧 Testing Reports Functionality (CRUD Operations)...\n')

async function testReportCreation() {
  console.log('1. 📝 Testing Report Creation...')
  
  try {
    // Find a super admin user
    const superAdmin = await prisma.user.findFirst({
      where: { role: 'SUPER_ADMIN' }
    })

    if (!superAdmin) {
      console.log('❌ No super admin found for testing')
      return null
    }

    // Create a test report
    const testReport = await prisma.report.create({
      data: {
        title: 'Test Report for CRUD Operations',
        description: 'This is a test report to verify CRUD functionality',
        type: 'CUSTOM',
        status: 'DRAFT',
        content: JSON.stringify({
          testData: {
            message: 'This is test content',
            timestamp: new Date().toISOString(),
            metrics: {
              testValue1: 100,
              testValue2: 200
            }
          }
        }),
        filters: JSON.stringify({
          testFilter: 'test-value',
          dateRange: 'last-30-days'
        }),
        isPublic: false,
        generatedBy: superAdmin.id
      },
      include: {
        generatedByUser: {
          select: {
            id: true,
            firstName: true,
            lastName: true,
            email: true
          }
        }
      }
    })

    console.log('✅ Report created successfully:')
    console.log(`   ID: ${testReport.id}`)
    console.log(`   Title: ${testReport.title}`)
    console.log(`   Type: ${testReport.type}`)
    console.log(`   Status: ${testReport.status}`)
    console.log(`   Generated by: ${testReport.generatedByUser.firstName} ${testReport.generatedByUser.lastName}`)
    
    return testReport
  } catch (error) {
    console.error('❌ Error creating report:', error)
    return null
  }
}

async function testReportReading(reportId: string) {
  console.log('\n2. 👁️ Testing Report Reading...')
  
  try {
    // Test reading single report
    const report = await prisma.report.findUnique({
      where: { id: reportId },
      include: {
        generatedByUser: {
          select: {
            id: true,
            firstName: true,
            lastName: true,
            email: true
          }
        },
        school: {
          select: {
            id: true,
            name: true,
            email: true
          }
        }
      }
    })

    if (report) {
      console.log('✅ Report read successfully:')
      console.log(`   ID: ${report.id}`)
      console.log(`   Title: ${report.title}`)
      console.log(`   Content length: ${report.content.length} characters`)
      console.log(`   Has filters: ${report.filters ? 'Yes' : 'No'}`)
      
      // Test content parsing
      try {
        const contentData = JSON.parse(report.content)
        console.log('✅ Content is valid JSON')
        console.log(`   Content keys: ${Object.keys(contentData).join(', ')}`)
      } catch (error) {
        console.log('⚠️ Content is not valid JSON')
      }
      
      // Test filters parsing
      if (report.filters) {
        try {
          const filtersData = JSON.parse(report.filters)
          console.log('✅ Filters are valid JSON')
          console.log(`   Filter keys: ${Object.keys(filtersData).join(', ')}`)
        } catch (error) {
          console.log('⚠️ Filters are not valid JSON')
        }
      }
    } else {
      console.log('❌ Report not found')
    }

    // Test reading multiple reports
    const reports = await prisma.report.findMany({
      take: 5,
      orderBy: { createdAt: 'desc' },
      include: {
        generatedByUser: {
          select: {
            firstName: true,
            lastName: true
          }
        }
      }
    })

    console.log(`✅ Found ${reports.length} reports in database`)
    
    return report
  } catch (error) {
    console.error('❌ Error reading report:', error)
    return null
  }
}

